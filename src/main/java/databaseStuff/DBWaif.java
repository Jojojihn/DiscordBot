package databaseStuff;

public class DBWaif {
    public String name;
    public String description;
    public int rarity;
    public int price;
    public String image;

    public DBWaif(String name, String description, int rarity, int price, String image) {
        this.name = name;
        this.description = description;
        this.rarity = rarity;
        this.price = price;
        this.image = image;
    }
}
